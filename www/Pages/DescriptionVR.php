                        Qu'est ce que la réalité virtuelle?

                    </p>

                    <p class='descriptionEntreprise'>

                        La réalité virtuelle est un dispositif numérique permettant à un usager d’être plongé, immergé, dans un environnement perçu comme réel, avec lequel il peut interagir. <br>

                        Ce dispositif est souvent matérialisé par un « casque » afin que l’usager ait le moins de contact avec l’extérieur. <br>

                        Plus le dispositif activera les sens de la personne, plus l’usager sera immergé dans l’environnement. <br><br>

                        Pour cela il existe d’autre matériels comme des gants de données (data glove),ou des systèmes à retour de force (bras haptique) mais également des dispositifs de capture du mouvement afin de pouvoir interagir avec le monde dans lequel il a été plongé. <br>

                        Il existe beaucoup de constructeurs de  "Casque de réalité virtuelle", mais les géants comme Sony, HTC ,Oculus et Samsung se partage la majorité du gâteau pour le marché du Gaming». 


                    </p>

